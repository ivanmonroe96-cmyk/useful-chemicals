def update_translation_file(lang_code, new_about_keys):
    filepath = os.path.join(TRANSLATIONS_DIR, f'{lang_code}.json')
    if not os.path.exists(filepath):
        print(f"  SKIP: {filepath} not found")
        return

    with open(filepath, 'r', encoding='utf-8') as f:
        data = json.load(f)

    # Get existing about section or create empty
    existing_about = data.get('about', {})

    # Remove old keys that are no longer used
    old_keys = [
        'hero_stat1', 'hero_stat2', 'hero_stat3', 'hero_explore', 'hero_contact',
        'mission_label', 'mission_title', 'mission_desc1', 'mission_desc2',
        'mission_feat1_title', 'mission_feat1_desc', 'mission_feat2_title', 'mission_feat2_desc',
        'why_label', 'why_title', 'why_desc',
        'why1_title', 'why1_desc', 'why2_title', 'why2_desc',
        'why3_title', 'why3_desc', 'why4_title', 'why4_desc',
        'facility_desc1', 'facility_desc2', 'facility_explore',
        'safety_label', 'safety_title',
        'safety1_title', 'safety1_desc', 'safety2_title', 'safety2_desc',
        'safety3_title', 'safety3_desc', 'safety4_title', 'safety4_desc',
        'cta_how_to_order',
    ]
    for key in old_keys:
        existing_about.pop(key, None)

    # Merge new keys (new keys take priority)
    existing_about.update(new_about_keys)
    data['about'] = existing_about

    with open(filepath, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        f.write('\n')

    print(f"  OK: {lang_code}.json updated with {len(new_about_keys)} new keys")


